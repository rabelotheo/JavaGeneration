package br.com.generation.aula01;

public class Exemplo02 {

	public static void main(String[] args) {

		int var1;
		double var2;
		
		var1 = 10;
		var2 = 10.0;
		
		System.out.println("Valor original da primeira variavel: " + var1);
		System.out.println("Valor original da segunda variavel: " + var2);
		System.out.println();
		
		var1 = var1 / 4;
		var2 = var2 / 4;
		
		System.out.println("Valor original da primeira variavel após a divisão: " + var1);
		System.out.println("Valor original da segunda variavel após a divisão:  " + var2);
		
	}

}
